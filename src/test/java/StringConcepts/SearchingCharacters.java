package StringConcepts;

public class SearchingCharacters {

	public static void main(String[] args) {
		
		String s="hemasahoo";
		System.out.println(s.indexOf('s'));//4
		System.out.println(s.indexOf("ah"));//5
		System.out.println(s.lastIndexOf("oo"));//7
		System.out.println(s.lastIndexOf('a'));//5
		System.out.println(s.contains("saho"));//true
		System.out.println(s.startsWith("h"));//true
		System.out.println(s.endsWith("o"));//true
		System.out.println(s.endsWith("a"));//false
		System.out.println(s.charAt(6));//h
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
